def _write_text_col(ws, col_idx: int):
    for r in range(2, ws.max_row + 1):
        ws.cell(r, col_idx).number_format = _n.FORMAT_TEXT


def make_collection_log():
    """
    수집일지_샘플1.xlsx / 수집일지_샘플2.xlsx — 각 25행, 총 50행
    실제 수집 데이터 특성 재현:
      7자리 병록번호, 가족 키워드 혼입, P+F 공존, F-only, 중복행, 날짜 오류 등
    1단계 실행 시 자동 전처리되는 내용을 샘플로 보여줌
    """
    for fname, rows in [
        ('수집일지_샘플1.xlsx', PATIENTS_1),
        ('수집일지_샘플2.xlsx', PATIENTS_2),
    ]:
        df = pd.DataFrame(rows, columns=COLS)
        out = OUT_DIR / fname
        with pd.ExcelWriter(out, engine='openpyxl') as w:
            df.to_excel(w, sheet_name='수집일지', index=False)
            ws = w.sheets['수집일지']
            _write_text_col(ws, COLS.index('병록번호') + 1)
        print(f'생성: {out.name} ({len(df)}행)')


